$(document).ready(function () {
  $(".content").hide();

  $("#design").click(function () {
    $(".content").slideToggle();
  });
  $(".development").hide();
  $("#dev").click(function () {
    $(".development").slideToggle();
  });
  $(".product-sample").hide();
  $("#product").click(function () {
    $(".product-sample").slideToggle();
  });
});
$(document).ready(function () {
  $("#work1")
    .mouseover(function () {
      $("#overlay").show();
      var overlay = document.getElementById("overlay");
      overlay.style.border = "3px white solid";
      overlay.style.width = "82%";
      overlay.style.height = "80%";
    })
    .mouseout(function () {
      $("#overlay").hide();
    });
});
